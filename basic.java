import java.util.Scanner;

public class basic {
    public static void main(String[] args) 
    {
        // Input
        Scanner  sc = new Scanner(System.in);
        
        // for taking input from user as integer

        int n = sc.nextInt();
        System.out.println(n);

        // for taking input from user as string
        String s = sc.next();
        System.out.println(s);

        // for taking input from user as string in one line
        String s1 = sc.nextLine();
        System.out.println(s1);
        

        
    }
}
